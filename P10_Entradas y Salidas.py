# Programa sencillo para calculadora básica
# Pedir dos valores al usuario
numero_a = float(input("Ingresa el primer valor: "))
numero_b = float(input("Ingresa el segundo valor: "))

# Realizar operaciones
resultado_suma = numero_a + numero_b
resultado_resta = numero_a - numero_b
resultado_multiplicacion = numero_a * numero_b
resultado_division = numero_a / numero_b

# Mostrar resultados
print("Suma:", resultado_suma)
print("Resta:", resultado_resta)
print("Multiplicación:", resultado_multiplicacion)
print("División:", resultado_division)

print("\nFin del programa.")