# Apuntes de operadores en Python

print("OPERACIONES BASICAS")

print("\nSuma:")
print(7 + 5)   # resultado: 12

print("\nResta:")
print(20 - 8)  # resultado: 12

print("\nMultiplicación:")
print(3 * 9)   # resultado: 27

print("\nDivisión real (/):")
print(9 / 4)   # siempre devuelve flotante

print("\nDivisión entera (//):")
print(15 // 4)  # elimina decimales

print("\nOperador módulo (%):")
print(17 % 5)   # residuo de la división

print("\nPotencia (**):")
print(3 ** 4)   # 3 elevado a la 4

print("\nOrden de operaciones:")
print(4 + 6 * 2)

print("\nUso de paréntesis para cambiar prioridad:")
print((4 + 6) * 2)

print("\nOperadores de signo:")
print(-10)
print(+10)

print("\nEjemplo de módulo múltiple:")
print(20 % 7 % 3)
# 20 % 7 = 6
# 6 % 3 = 0

print("\nPotencias encadenadas:")
print(3 ** 2 ** 2)